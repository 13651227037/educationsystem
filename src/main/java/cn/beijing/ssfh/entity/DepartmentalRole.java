package cn.beijing.ssfh.entity;

public class DepartmentalRole {
    private Integer departmentalRoleId;

    private Integer departmentId;

    private Integer roleId;

    public Integer getDepartmentalRoleId() {
        return departmentalRoleId;
    }

    public void setDepartmentalRoleId(Integer departmentalRoleId) {
        this.departmentalRoleId = departmentalRoleId;
    }

    public Integer getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(Integer departmentId) {
        this.departmentId = departmentId;
    }

    public Integer getRoleId() {
        return roleId;
    }

    public void setRoleId(Integer roleId) {
        this.roleId = roleId;
    }
}